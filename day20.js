function parseGrid(lines) {
  let result = [];
  for (const line of lines) {
    result.push(line.split(''));
  }
  return result;
}
function expandGrid(grid, fillValue = '.') {
  let result = [];
  result.push(new Array(grid[0].length+2).fill(fillValue));
  for (const row of grid) {
    let expandedRow = Array.from(row);
    expandedRow.unshift(fillValue);
    expandedRow.push(fillValue);
    result.push(expandedRow);
  }
  result.push(Array.from(result[0])); // duplicate first row
  return result;
}
function printGrid(grid) {
  return grid.map(r => r.join('')).join('\n');
}
console.assert(printGrid(parseGrid(['.#','..'])) === '.#\n..', 'print(parse(x)) failed symmetry');
console.assert(printGrid(expandGrid(parseGrid(['##']))) === ['....\n.##.\n....'].join(), 'expandGrid failed');

function countLitRow(row) {
  return row.reduce((prev, curr) => prev + ((curr === '#') ? 1 : 0), 0);
}
console.assert(countLitRow(parseGrid(['.##..'])[0]) === 2, 'countLitRow failed');
function countLitGrid(grid) {
  return grid.reduce((prev, curr) => prev + countLitRow(curr), 0);
}
